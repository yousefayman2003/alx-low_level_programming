#include "3-calc.h"

/**
 * op_add - adds two int
 * @a: first int
 * @b: second int
 * Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two int
 * @a: first int
 * @b: second int
 * Return: subtraction
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- multiply two int
 * @a: first int
 * @b: second int
 * Return: multiplication
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two int
 * @a: first int
 * @b: second int
 * Return: division
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two int
 * @a: first int
 * @b: second int
 * Return: remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
